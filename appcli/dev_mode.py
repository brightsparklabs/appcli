# ------------------------------------------------------------------------------
# IMPORTS
# ------------------------------------------------------------------------------

import sys

import pyfiglet

# ------------------------------------------------------------------------------
# CONSTANTS
# ------------------------------------------------------------------------------

__BANNER = pyfiglet.figlet_format("DEV MODE", font="tinker-toy")

# ------------------------------------------------------------------------------
# METHODS
# ------------------------------------------------------------------------------


def print_dev_mode(message: str):
    """Prints a DEV MODE message under a large banner.

    Args:
      message (str): Message to print.
    """

    print(
        f"""
{"*" * 80}

{__BANNER}
{message}

// DEV MODE
{"*" * 80}

""",
        file=sys.stderr,
    )
